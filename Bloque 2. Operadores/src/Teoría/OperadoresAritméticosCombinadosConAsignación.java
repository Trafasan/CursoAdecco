public class OperadoresAritméticosCombinadosConAsignación {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int númeroS=10;		
		númeroS+=5;//Esto es igual a númeroS=númeroS+5		
		System.out.println("El resultado de la suma es: "+númeroS);
		
		int númeroR=13;		
		númeroR-=5;//Esto es igual a númeroR=númeroR-5		
		System.out.println("El resultado de la resta es: "+númeroR);
		//Este tipo de expresiones sirven para todos los operadores aritméticos

	}

}
